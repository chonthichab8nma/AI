// ✅ 6. ฟังก์ชันเพื่อไปยังสถานที่ถัดไปและก่อนหน้า
function goToNextPlace() {
    const placeNames = Object.keys(placesData);  // ดึงชื่อสถานที่ทั้งหมด
    const currentIndex = placeNames.indexOf(placeName);  // หาตำแหน่งของสถานที่ปัจจุบัน
    const nextIndex = (currentIndex + 1) % placeNames.length;  // ถ้ากดไปสุดท้ายจะไปที่แรก
    window.location.href = `?place=${placeNames[nextIndex]}`;  // ไปยังสถานที่ถัดไป
}

function goToPreviousPlace() {
    const placeNames = Object.keys(placesData);
    const currentIndex = placeNames.indexOf(placeName);
    const prevIndex = (currentIndex - 1 + placeNames.length) % placeNames.length;  // ถ้ากดไปแรกจะไปที่สุดท้าย
    window.location.href = `?place=${placeNames[prevIndex]}`;  // ไปยังสถานที่ก่อนหน้า
}

